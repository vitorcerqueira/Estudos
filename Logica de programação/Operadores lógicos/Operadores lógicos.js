//OPERADORES LOGICOS
// E (AND)
// Ou (OR)
// Não (NOT)

//TABELA VERDADE - E (AND)
// operador &&
// so é verdadeiro se ambas as partes forem verdadeiras
//true && true === true
//true && false === false
//falso && true === false
//falso && false === false

//TABELA VERDADE - Ou (OR)
// operador ||
// so é falso se ambas as partes forem falsas
//true || true === true
//true || false === true
//falso || true ===  true
//falso || false === false

//TABELA VERDADE - Não (NOT)
// operador !
// inverte o resultado (de verdadeiro para falso e vice-versa)
//true ! false === false
//false ! true === true
